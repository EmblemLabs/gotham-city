use std::io::Write;
use chrono::Local;
use env_logger::Builder;
use log::LevelFilter;
use server_lib::server;

fn main() {
    Builder::new()
        .format(|buf, record| {
            writeln!(buf,
                     "{} [{}] - {}",
                     Local::now().format("%Y-%m-%dT%H:%M:%S"),
                     record.level(),
                     record.args()
            )
        })
        .filter(None, LevelFilter::Info)
        .init();
    server::get_server().launch();
}
